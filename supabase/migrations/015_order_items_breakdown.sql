-- 015: يضيف عمود order_items لجدول orders — كل عنصر بالمصفوفة يمثّل
-- قطعة مستقلة (حذاء، حقيبة...) مع نوع الخدمة المطلوبة لها، وصفها،
-- والفني المسؤول عنها. هذا يدعم الميزة الجديدة بشاشة "إنشاء طلب":
-- عميل عنده أكثر من قطعة (حذاءين مثلاً) بشغلات مختلفة (تنظيف/تنعيل)
-- وفنيين مختلفين — بدل ما يُجبر الموظف يسجّلها كطلب واحد عام بدون
-- تفاصيل لكل قطعة.
-- إضافة عمود فقط (لا كسر لأي كود قديم يعتمد على item_type/quantity
-- الحاليين — يستمرون يُحسبون تلقائياً من عدد القطع).
-- ─────────────────────────────────────────────────────────────────
ALTER TABLE orders ADD COLUMN IF NOT EXISTS order_items JSONB DEFAULT '[]'::jsonb;
CREATE INDEX IF NOT EXISTS idx_orders_order_items ON orders USING GIN (order_items);
